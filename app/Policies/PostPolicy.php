    }

    // Get permissions function
    protected function getPermission($user,$p_id)
   {
       foreach ($user->roles as $role) {
           foreach ($role->permissions as $permission) {
               if ($permission->id == $p_id) {
                   return true;
               }
           }
       }
       return false;
   }
